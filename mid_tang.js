function 調整音韻地位() {
  function 調整(表達式, 調整屬性, 字頭串 = null) {
    if (typeof (字頭串) === 'string' && !字頭串.includes(字頭)) return;
    if (is(表達式)) 音韻地位 = 音韻地位.調整(調整屬性);
  }

  // 輕唇化例外
  調整('明母 尤韻', { 等: '一', 類: null, 韻: '侯' });
  調整('明母 東韻', { 等: '一', 類: null });

  if (is`云母 通攝 舒聲`) 音韻地位 = 音韻地位.調整('匣母', ['匣母三等']); // 雄熊

  // [慧琳反切體現的, 唐代用韻體現的, 據今音推測的]
  const 蟹攝二等入假攝字 = ['崖咼(呙)扠涯搋派差絓畫(画)罣罷(罢)', '佳鼃娃解釵(钗)卦柴', '哇洼蛙灑蝸話(话)掛挂查叉杈衩'].join('');
  const 流攝脣音入遇攝字 = ['浮戊母罦罘蜉矛茂覆懋拇某負(负)阜', '謀(谋)部畝(亩)畮婦(妇)不否桴富牟缶', '復複(复)副牡'].join('');
  if (選項.部分蟹二開入假攝 !== false) {
    調整('佳韻 合口 非 上聲', { 韻: '麻' });
    調整('蟹攝 二等', { 韻: '麻' }, 蟹攝二等入假攝字);
  }
  if (選項.部分流攝脣音入遇攝 !== false) {
    調整('幫組 尤侯韻', { 韻: is`尤韻` ? '虞' : '模' }, 流攝脣音入遇攝字);
  }
}

調整音韻地位();

const 韻圖等 = when([
  [!選項.幽韻一律歸四等 && '幽韻 B類', '三'],
  [!選項.完全莊三化二 && '莊組 三等', '三'],
  ['精組 止攝 開口', '一'], // 實際上無用，因爲 i 前的 j 本身就會被省略
  ['', 音韻地位.韻圖等],
]);

function get聲母() {
  let 等類記法 = 選項.等類記法.split(',');
  let 聲母 = when([
    ['幫組 C類', 非組字典[選項.非組][音韻地位.母]],
    [選項.常船合併崇俟合併 !== false && '俟母', 'dʐ'],
    [選項.常船合併崇俟合併 !== false && '常母', 'ʑ'],
    [!選項.見組一二等簡寫作軟腭音 && '見溪疑曉匣母 一二等', {
      見: 'q', 溪: 'qʰ', 疑: 'ɴ', 曉: 'χ', 匣: 'ʁ',
    }[音韻地位.母]],
    ['', {
      幫: 'p', 滂: 'pʰ', 並: 'b', 明: 'm',
      端: 't', 透: 'tʰ', 定: 'd', 泥: 'n', 來: 'l',
      知: 'ʈ', 徹: 'ʈʰ', 澄: 'ɖ', 孃: 'ɳ',
      精: 'ts', 清: 'tsʰ', 從: 'dz', 心: 's', 邪: 'z',
      莊: 'tʂ', 初: 'tʂʰ', 崇: 'dʐ', 生: 'ʂ', 俟: 'ʐ',
      章: 'tɕ', 昌: 'tɕʰ', 常: 'dʑ', 書: 'ɕ', 船: 'ʑ', 日: 'ɲ', 以: 'j',
      見: 'k', 溪: 'kʰ', 羣: 'ɡ', 疑: 'ŋ',
      影: 'ʔ', 曉: 'x', 匣: 'ʁ', 云: '',
    }[音韻地位.母]],
  ]) + when([
    ['幫組 C類', ''],
    ['莊章組 或 日以母', ''],
    [韻圖等 === '四', 等類記法[0]],
    [韻圖等 === '三', 等類記法[1]],
    ['', 等類記法[2]],
  ]);
  聲母 = 聲母.replace(/ʰ([ʲˠ])/, '$1ʰ');
  聲母 = 聲母.replace(/ʁʲ/, 'ɣʲ');
  聲母 = 聲母.replace(/ʁˠ/, 'ɣ');
  聲母 = 聲母.replace(/^ˠ/, 'ɰ');
  return 聲母;
}

function get介音() {
  return is`合口` ? 'w' : '';
}

function get韻基() {
  let 韻基 = when([
    ['遇攝 非 開口', 'u'],
    ['遇攝', 'ɨ'],
    ['假攝', 'æ'], ['果攝', 'ɑ'],

    ['止攝', 'i'], ['流攝', 'ɨw'],
    ['蟹攝', 'ɛj'], ['效攝', 'ɛw'],

    ['臻攝 (B類 合口 或 C類 非 開口)', 'un'],
    ['臻攝', 'in'], ['深攝', 'im'],
    ['山攝', 'ɛn'], ['咸攝', 'ɛm'],

    ['通攝 幫組 C類 或 鍾韻', 'uŋ'], // 鍾韻底層形式爲 /wɨwŋ/
    ['曾攝', 'ɨŋ'], ['通攝', 'ɨwŋ'],
    ['梗攝', 'ɛŋ'], ['宕攝', 'ɑŋ'], ['江攝', 'æwŋ'],
  ]);
  if (韻圖等 === '一') 韻基 = 韻基.replace(/i(?=[^ŋ])/, 'ɨ');
  韻基 = 韻基.replace('ɨ', 選項.後高元音.split(',')[+is`魚韻`]);
  if (韻圖等 === '二') 韻基 = 韻基.replace(/ɛ(?=[^ŋ])/, 'æ');
  if (韻圖等 === '一') 韻基 = 韻基.replace(/ɛ(?=[^ŋ])/, 'ɑ');
  韻基 = 韻基.replace('æ', 選項.低元音.split(',')[0]);
  韻基 = 韻基.replace('ɑ', 選項.低元音.split(',')[1]);
  if (is`入聲`) [...'mnŋɴ'].forEach((v, i) => { 韻基 = 韻基.replace(v, 'ptkq'[i]); });
  return 韻基;
}

function get聲調() {
  const is陰 = is`全清 或 次清 或 次濁 上入聲`;
  return {
    五度符號: is陰 ?
      { 平: '˦˨', 上: '˦˥', 去: '˧˨˦', 入: '˦' } :
      { 平: '˨˩', 上: '˨˦', 去: '˨˨˦', 入: '˨' },
    附加符號: { 平: '̀', 上: '́', 去: '̌', 入: '' },
    調類數字: { 平: '¹', 上: '²', 去: '³', 入: '⁴' },
  }[選項.聲調][選項.全濁上歸去 && is`全濁 上聲` ? '去' : 音韻地位.聲];
}

function get音節() {
  const 音節 = {
    聲母: get聲母(),
    介音: get介音(),
    韻基: get韻基(),
    聲調: get聲調(),
  };
  if (音節.韻基[0] === 'i') 音節.聲母 = 音節.聲母.replace(/(?<!^)j/, '');
  if (音節.介音 === 'w') 音節.聲母 = 音節.聲母.replace('ɰ', '');
  if (音節.韻基[0] === 'u') 音節.介音 = 音節.介音.replace('w', '');
  音節.韻母 = 音節.介音 + 音節.韻基;
  音節.帶調韻基 = 選項.聲調 === '附加符號' ?
    音節.韻基[0] + 音節.聲調 + 音節.韻基.slice(1) :
    音節.韻基 + 音節.聲調;
  return 音節;
}

const 音節 = get音節();
return 音節.聲母 + 音節.介音 + 音節.帶調韻基;
